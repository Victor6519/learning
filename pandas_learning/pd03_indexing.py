"""
Pandas 模块3：索引与筛选
=============================
loc (标签), iloc (位置), 布尔筛选, query, isin。
"""

import pandas as pd
import numpy as np

# 构建示例数据
df = pd.DataFrame({
    '姓名':  ['张三', '李四', '王五', '赵六', '孙七'],
    '年龄':  [25, 30, 22, 28, 35],
    '城市':  ['北京', '上海', '广州', '深圳', '北京'],
    '工资':  [8000, 12000, 6500, 9500, 15000],
    '部门':  ['研发', '销售', '研发', '销售', '管理'],
})
df.index = ['a', 'b', 'c', 'd', 'e']  # 自定义行标签
print(f"数据:\n{df}\n")

# ============================================================
# 1. loc — 按标签索引 (包含右边界!)
# ============================================================
print("1. loc — 按标签")
print("=" * 60)

print(f"   loc['a']:\n{df.loc['a']}\n")                  # 单行
print(f"   loc['a':'c']:\n{df.loc['a':'c']}\n")           # 行切片 (含c)
print(f"   loc['a', '工资'] = {df.loc['a', '工资']}\n")    # 单元格
print(f"   loc[['a','c'], ['姓名','工资']]:\n"
      f"{df.loc[['a','c'], ['姓名','工资']]}\n")          # 指定行列

# ============================================================
# 2. iloc — 按位置索引 (和 NumPy 一样, 不含右边界)
# ============================================================
print("2. iloc — 按位置")
print("=" * 60)

print(f"   iloc[0]:\n{df.iloc[0]}\n")                     # 第1行
print(f"   iloc[0:2]:\n{df.iloc[0:2]}\n")                  # 前2行 (不含2)
print(f"   iloc[0, 3] = {df.iloc[0, 3]}\n")                # (0行,3列)
print(f"   iloc[[0,2,4], [0,3]]:\n"
      f"{df.iloc[[0,2,4], [0,3]]}\n")                     # 指定行列

# ============================================================
# 3. 布尔筛选 (最常用!)
# ============================================================
print("3. 布尔筛选")
print("=" * 60)

print(f"   工资 > 9000:\n{df[df['工资'] > 9000]}\n")
print(f"   城市 == '北京':\n{df[df['城市'] == '北京']}\n")

# 多条件: & | ~ (不是 and or not!)
print(f"   工资>8000 且 部门=='研发':\n"
      f"{df[(df['工资'] > 8000) & (df['部门'] == '研发')]}\n")

# 多值筛选: isin
print(f"   城市在北京或上海:\n{df[df['城市'].isin(['北京', '上海'])]}\n")

# 字符串筛选: str.contains
print(f"   姓名包含 '三':\n{df[df['姓名'].str.contains('三')]}\n")

# ============================================================
# 4. query — SQL风格筛选
# ============================================================
print("4. query() — SQL 风格")
print("=" * 60)

result = df.query('工资 > 8000 and 城市 == "北京"')
print(f"   query('工资 > 8000 and 城市 == \"北京\"'):\n{result}\n")

# 用变量: @变量名
threshold = 10000
result2 = df.query('工资 > @threshold')
print(f"   query('工资 > @threshold'), threshold={threshold}:\n{result2}\n")

# ============================================================
# 5. where / mask — 条件替换
# ============================================================
print("5. where / mask")
print("=" * 60)

# where: 条件为 False 的替换为 NaN
print(f"   where(工资>9000):\n{df['工资'].where(df['工资'] > 9000)}\n")

# mask: 和 where 相反 (条件为 True 的替换)
print(f"   mask(工资>9000):\n{df['工资'].mask(df['工资'] > 9000, '高薪')}\n")

print("[OK] Pandas 模块3 完成!")
